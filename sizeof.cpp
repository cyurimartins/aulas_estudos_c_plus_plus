#include <iostream>
using namespace std;

int main(int argc, char** argv) {
	
	cout <<" --- Tamanho das variaveis --- \n"<<endl;
	cout << "Tamanho de int: "<<sizeof(int)<<" bytes."<<endl;
	cout << "Tamanho de short int: "<<sizeof(short)<<" bytes."<<endl;
	cout << "Tamanho de long int: "<<sizeof(long)<<" bytes."<<endl;
	cout << "Tamanho de float: "<<sizeof(float)<<" bytes."<<endl;
	cout << "Tamanho de double: "<<sizeof(double)<<" bytes."<<endl;
	cout << "Tamanho de bool: "<<sizeof(bool)<<" bytes."<<endl;
	cout << "Tamanho de char: "<<sizeof(char)<<" bytes."<<endl;
	
	
	return 0;
}
